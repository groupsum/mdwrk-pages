function parseCsv(text) {
  const rows = [];
  let row = [];
  let field = "";
  let inQuotes = false;

  for (let index = 0; index < text.length; index += 1) {
    const char = text[index];

    if (inQuotes) {
      if (char === "\"") {
        if (text[index + 1] === "\"") {
          field += "\"";
          index += 1;
        } else {
          inQuotes = false;
        }
      } else {
        field += char;
      }
      continue;
    }

    if (char === "\"") {
      inQuotes = true;
      continue;
    }

    if (char === ",") {
      row.push(field);
      field = "";
      continue;
    }

    if (char === "\r") continue;

    if (char === "\n") {
      row.push(field);
      rows.push(row);
      row = [];
      field = "";
      continue;
    }

    field += char;
  }

  if (field.length > 0 || row.length > 0) {
    row.push(field);
    rows.push(row);
  }

  const [header, ...body] = rows;
  return body
    .filter((record) => record.some((value) => value.length > 0))
    .map((record) => Object.fromEntries(header.map((key, index) => [key, record[index] ?? ""])));
}

function uriLabel(uri) {
  return uri.trim().replace(/^https:\/\/schema\.org\//, "");
}

function splitSchemaList(value) {
  return value
    .split(",")
    .map((part) => uriLabel(part))
    .filter(Boolean);
}

const typesRows = parseCsv(fs.readFileSync(typesCsvPath, "utf8"));
const propertiesRows = parseCsv(fs.readFileSync(propertiesCsvPath, "utf8"));

const typeProperties = Object.fromEntries(
  SUPPORTED_TYPES.map((type) => {
    const row = typesRows.find((entry) => entry.label === type);
    if (!row) {
      throw new Error(`Missing Schema.org type row for ${type}`);
    }
    return [type, splitSchemaList(row.properties).sort()];
  }),
);

const propertyRanges = Object.fromEntries(
  propertiesRows.map((row) => [row.label, splitSchemaList(row.rangeIncludes).sort()]),
);

const fileContents = `// Generated by scripts/generate-schemaorg-vocabulary.mjs. Do not edit by hand.
export const SCHEMAORG_SUPPORTED_TYPES = ${JSON.stringify(SUPPORTED_TYPES, null, 2)} as const;

export const SCHEMAORG_TYPE_PROPERTIES = ${JSON.stringify(typeProperties, null, 2)} as const;

export const SCHEMAORG_PROPERTY_RANGES = ${JSON.stringify(propertyRanges, null, 2)} as const;
`;

fs.writeFileSync(outputPath, fileContents.replaceAll("\r\n", "\n"), "utf8");
