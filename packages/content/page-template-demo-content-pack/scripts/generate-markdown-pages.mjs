import { mkdir, readFile, readdir, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const packageRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const pagesRoot = path.join(packageRoot, "content", "pages");
const outputPath = path.join(packageRoot, "src", "generated", "markdown-pages.ts");

const fileOrder = new Map([
  ["demo-home.md", 0],
  ["demo-feature.md", 1],
  ["demo-pricing.md", 2],
  ["demo-privacy.md", 3],
]);

const files = (await readdir(pagesRoot))
  .filter((file) => file.endsWith(".md"))
  .sort((left, right) => {
    const leftOrder = fileOrder.get(left) ?? Number.MAX_SAFE_INTEGER;
    const rightOrder = fileOrder.get(right) ?? Number.MAX_SAFE_INTEGER;
    return leftOrder - rightOrder || left.localeCompare(right);
  });

const pages = await Promise.all(files.map(async (file) => {
  const relativePath = path.posix.join("content/pages", file);
  const raw = await readFile(path.join(pagesRoot, file), "utf8");
  return { path: relativePath, raw };
}));

const source = `// Generated by scripts/generate-markdown-pages.mjs from content/pages/*.md.
// Do not edit by hand.

import type { MarkdownPageTemplateSourceFile } from "@mdwrk/lander-page-templates";

export const pageTemplateDemoMarkdownFiles = Object.freeze(${JSON.stringify(pages, null, 2)}) satisfies readonly MarkdownPageTemplateSourceFile[];
`;

await mkdir(path.dirname(outputPath), { recursive: true });
await writeFile(outputPath, source, "utf8");
